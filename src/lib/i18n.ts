export const useTranslation = (language: Language) => {
  const t = (key: string): string => {
    const value = translations[language][key as keyof typeof translations[typeof language]]
    return typeof value === 'string' ? value : key
  }

  const getMonths = (): string[] => {
    return translations[language].months
  }

  const getDays = (): string[] => {
    return translations[language].days
  }

  const getYearSuffix = (): string => {
    return translations[language].yearSuffix
  }

  return { t, getMonths, getDays, getYearSuffix }
}